    public void showTable() {
        int i=1;
        for (HoaDonDTO s : list) {
            model.addRow(new Object[]{
                i++, s.getMaHoaDon(), s.getMaNV(), s.getSoLuong(), s.getThoiGian(),s.getNgayLapHD()
            });
        }
     }
    public void showTableCTHD() {
        int i=1;
        for (CTHDDTO s : list1) {
            model1.addRow(new Object[]{
                i++, s.getMaHoaDon(), s.getTenMon(), s.getMaMon(), s.getSoLuong(), s.getGia()
            });
        }
    }
    public void clearTable() {
        for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
            model.removeRow(i);
        }
    }
   public void clearTableCTHD() {
        for( int i = model1.getRowCount() - 1; i >= 0; i-- ) {
            model1.removeRow(i);
        }
    }
