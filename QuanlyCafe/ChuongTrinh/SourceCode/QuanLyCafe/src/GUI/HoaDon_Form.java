    public void showDB(){
        int selectrow=tbHoaDon.getSelectedRow();     
        txtMaHoaDon.setText(tbHoaDon.getValueAt(selectrow,1).toString());
        txtMaNV.setText(tbHoaDon.getValueAt(selectrow,2).toString());
        txtSoLuong.setText(tbHoaDon.getValueAt(selectrow,3).toString());
        txtThoiGian.setText(tbHoaDon.getValueAt(selectrow,4).toString());
        Date date;
        try {
            date = new  SimpleDateFormat("dd/MM/yyyy").parse((String)model.getValueAt(selectrow,5));
            txtDate.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(HoaDon_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
