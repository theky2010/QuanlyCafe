    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        FileOutputStream out = null;
        try {
            XSSFWorkbook workbook = new XSSFWorkbook();
            XSSFSheet spreadsheet = workbook.createSheet("ThongKeHoaDon");
            XSSFRow row = null;
            XSSFCell cell = null;
            int width = 10; // Where width is number of caracters 
            spreadsheet.setDefaultColumnWidth(width);
            /*CellStyle cellStyle = workbook.createCellStyle();
            cellStyle.setVerticalAlignment(VerticalAlignment.CENTER);
            cellStyle.setAlignment(HorizontalAlignment.LEFT);
            cell.setCellStyle(cellStyle);*/
            row = spreadsheet.createRow((short) 1);//hàng 2
            row.setHeight((short) 300);
            cell = row.createCell(1, CellType.STRING);//
            cell.setCellValue("THỐNG KÊ HÓA ĐƠN");
            row = spreadsheet.createRow((short) 3); //cột 3
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("STT");
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue("NgayLapHD");
            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue("SoLuong");
            for (int i = 0; i < list.size(); i++) {  
                row = spreadsheet.createRow((short) 4 + i);
                row.setHeight((short) 300);
                row.createCell(0).setCellValue(i + 1);
                row.createCell(1).setCellValue(list.get(i).getNgayLapHD());
                row.createCell(2).setCellValue(list.get(i).getSoLuong());
            }
            out = new FileOutputStream(new File("E:/thongkehoadon.xlsx"));
            workbook.write(out);
            out.close();
            JOptionPane.showMessageDialog(null,"Xuất thành công");
        } catch (Exception ex) {
           ex.printStackTrace();
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

