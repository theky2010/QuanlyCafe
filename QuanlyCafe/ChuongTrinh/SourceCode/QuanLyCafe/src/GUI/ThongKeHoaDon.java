    public void clearTable() {
        for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
            model.removeRow(i);
        }
    }
    public void showTable() {
        int i=1;
        for (HoaDonDTO s : list) {
            model.addRow(new Object[]{
                i++, s.getNgayLapHD(),s.getSoLuong()
            });
        }
    }
//biểu đồ thống kê
    public void setDataToChart1(JPanel jpnItem) {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        if (list != null) {
            for (HoaDonDTO s : list) {
                dataset.addValue(s.getSoLuong(), "Hóa Đơn",s.getNgayLapHD() );
                tong+=Integer.valueOf(s.getSoLuong().toString());
            }
        }
        JFreeChart barChart = ChartFactory.createBarChart(
                "Biểu đồ thống kê số lượng hóa đơn".toUpperCase(),
                "Thời gian", "Số lượng",
                dataset, PlotOrientation.VERTICAL, true, true, false);
        ChartPanel chartPanel = new ChartPanel(barChart);
        chartPanel.setPreferredSize(new Dimension(jpnItem.getWidth(), 320));
        //chartPanel.setPreferredSize(new Dimension(jpnItem.getHeight(), 600));
        jpnItem.removeAll();
        jpnItem.setLayout(new CardLayout());
        jpnItem.add(chartPanel);
        jpnItem.validate();
        jpnItem.repaint();
        
    }
