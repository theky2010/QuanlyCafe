    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        String keyString=jTextFieldSearch.getText();
        if(keyString.equals("")){
            JOptionPane.showMessageDialog(null, "Hãy nhập mã nhân viên cần tìm");
            jTextFieldSearch.requestFocus(true);
        }else {
            tbBangLuong.removeAll();
            tbBangLuong.revalidate();
            tbBangLuong.repaint();
            DefaultTableModel defaultTableModel=new DefaultTableModel();
            tbBangLuong.setModel(defaultTableModel);           
//setting lại table bảng lương
            BangLuongBUS blBus=new BangLuongBUS();
            try {
                ArrayList<BangLuongDTO> bangluong=blBus.searchBangLuong(keyString);
                defaultTableModel.addColumn("STT");
                defaultTableModel.addColumn("MaNV");
                defaultTableModel.addColumn("TienThuong");
                defaultTableModel.addColumn("TamUng");
                defaultTableModel.addColumn("Luong");
                int i=1;
                for (BangLuongDTO bl1: bangluong){
                    defaultTableModel.addRow(new Object[]{
                        i++, bl1.getMaNV(), bl1.getTienThuong(), bl1.getTamUng(), bl1.getLuong()
                    });
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
