    public void showTable() {
        int i=1;
        for (BangLuongDTO s : list) {
            model.addRow(new Object[]{
                i++, s.getMaNV(), s.getTienThuong(), s.getTamUng(),s.getLuong()
            });
        }
     }
     public void clearTable() {
    for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
        model.removeRow(i);
    }
   }
