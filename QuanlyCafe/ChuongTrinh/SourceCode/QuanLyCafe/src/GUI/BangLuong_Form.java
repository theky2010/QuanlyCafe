    private void bt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt1ActionPerformed
        // TODO add your handling code here:
        BangLuongDTO s= new BangLuongDTO();
        s.setMaNV(txtMaNV.getText());
        s.setTienThuong(Integer.parseInt(txtTienThuong.getText()));
        s.setTamUng(Integer.parseInt(txtTamUng.getText()));
        s.setLuong(Integer.parseInt(txtLuong.getText()));
        if (new BangLuongBUS().addBangLuong(s)) {
            JOptionPane.showMessageDialog(null, "Thêm Thành Công.");
            list.add(s);
        } else {
            JOptionPane.showMessageDialog(null, "Thêm Thất Bại.");
        }
        clearTable();
        list = new BangLuongBUS().getListBangLuong();
        showTable();
        
    }//GEN-LAST:event_bt1ActionPerformed
