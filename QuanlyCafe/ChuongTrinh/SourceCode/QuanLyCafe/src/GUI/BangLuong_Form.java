    private void bt2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt2ActionPerformed
        // TODO add your handling code here:
        BangLuongDTO s=new BangLuongDTO();
        int o1=Integer.valueOf(txtTienThuong.getText());
        int o2=Integer.valueOf(txtTamUng.getText());
        int luong=Integer.valueOf(txtLuong.getText());
        s.setMaNV(txtMaNV.getText());
        s.setTienThuong(o1);
        s.setTamUng(o2);
        s.setLuong(luong+o1-o2);
        if (new BangLuongBUS().updateBangLuong(s)){
            JOptionPane.showMessageDialog(null, "Sửa thành công");
        }
        else JOptionPane.showMessageDialog(null, "Sửa thất bại");
        clearTable();
        list=new BangLuongBUS().getListBangLuong();
        showTable();
            
    }//GEN-LAST:event_bt2ActionPerformed
