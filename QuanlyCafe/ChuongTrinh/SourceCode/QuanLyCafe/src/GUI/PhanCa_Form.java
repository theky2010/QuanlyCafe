    public void showTable() {
        int i=1;
        for (PhanCaDTO s : list) {
            model.addRow(new Object[]{
                i++, s.getMaNV(), s.getHeSoLuong(), s.getSoGio()
            });
        }
     }
     public void clearTable() {
    for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
        model.removeRow(i);
    }
   }
