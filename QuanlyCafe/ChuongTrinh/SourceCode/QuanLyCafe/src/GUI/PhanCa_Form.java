    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String keyString=jTextFieldSearch.getText();
        if(keyString.equals("")){
            JOptionPane.showMessageDialog(null, "Hãy nhập mã nhân viên cần tìm");
            jTextFieldSearch.requestFocus(true);
        }else {
            tbPhanCa.removeAll();
            tbPhanCa.revalidate();
            tbPhanCa.repaint();
            DefaultTableModel defaultTableModel=new DefaultTableModel();
            tbPhanCa.setModel(defaultTableModel);           
//setting lại table phân ca
            PhanCaBUS pcBus=new PhanCaBUS();
            try {
                ArrayList<PhanCaDTO> phanca=pcBus.searchPhanCa(keyString);
                defaultTableModel.addColumn("STT");
                defaultTableModel.addColumn("MaNV");
                defaultTableModel.addColumn("HeSoLuong");
                defaultTableModel.addColumn("SoGio");
                int i=1;
                for (PhanCaDTO pc1: phanca){
                    defaultTableModel.addRow(new Object[]{
                        i++, pc1.getMaNV(), pc1.getHeSoLuong(), pc1.getSoGio()
                    });
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed
