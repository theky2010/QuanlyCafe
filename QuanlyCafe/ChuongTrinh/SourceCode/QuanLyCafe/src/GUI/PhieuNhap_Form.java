    public String getMaNhap(){
        String x=list.get(list.size()-1).getMaNhap().toString();
        String xx=x.substring(3);
        int ma=Integer.valueOf(xx);
        ma++;
        x=x.replaceFirst(xx,String.valueOf(ma)); 
        return x;
    }
    public String getDate(){
        Date x= new Date();
        return fdate.format(x);
    }
    public void showTable() {
        int i=1;
        for (ChiTietNhapDTO s : list) {
            model.addRow(new Object[]{
                i++, s.getMaNhap(), s.getMaMon(), s.getSoLuong(), s.getDVT(),s.getTongTien(),s.getNgayNhap()
            });
        }
     }
     public void clearTable() {
        for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
            model.removeRow(i);
        }
   }    

