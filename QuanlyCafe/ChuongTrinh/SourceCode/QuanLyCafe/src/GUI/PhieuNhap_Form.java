    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String keyString=jTextFieldSearch.getText();
        if(keyString.equals("")){
            JOptionPane.showMessageDialog(null, "Hãy nhập mã phiếu nhập cần tìm");
            jTextFieldSearch.requestFocus(true);
        }else {
            tbPhieuNhap.removeAll();
            tbPhieuNhap.revalidate();
            tbPhieuNhap.repaint();
            DefaultTableModel defaultTableModel=new DefaultTableModel();
            tbPhieuNhap.setModel(defaultTableModel);           
//setting lại table phiếu nhập
            ChiTietNhapBUS ctnhapBus=new ChiTietNhapBUS();
            try {
                ArrayList<ChiTietNhapDTO> ctnhap=ctnhapBus.searchPhieuNhap(keyString);
                defaultTableModel.addColumn("STT");
                defaultTableModel.addColumn("MaNhap");
                defaultTableModel.addColumn("MaMon");
                defaultTableModel.addColumn("SoLuong");
                defaultTableModel.addColumn("DVT");
                defaultTableModel.addColumn("TongTien");
                defaultTableModel.addColumn("NgayNhap");
                int i=1;
                for (ChiTietNhapDTO ctnhap1: ctnhap){
                    defaultTableModel.addRow(new Object[]{
                        i++, ctnhap1.getMaNhap(), ctnhap1.getMaMon(), ctnhap1.getSoLuong(), ctnhap1.getDVT(), ctnhap1.getTongTien(), ctnhap1.getNgayNhap()
                    });
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed
